			      <p>Shelf-life is a simple function of controlling any organic activity in food. When we cook food at home and leave it out for a few hours, it spoils. From a scientific perspective, food spoilage can be controlled in one of three ways. You can freeze it, like storing food in our refrigerators at home. You can create an environment by infusing oil, preservatives or chemicals which would restrict any organic activity. Or you can simply neutralize all possible sources of the organic activity.<br/>
				We adopt the third option. We didn’t invent it – it’s been around for ages but is often not used because infusing chemicals or sugar is much more cost effective. And it’s difficult to master this process if you want to retain the taste in the food.<br/><br/>
				What really happens is, we cook the food the way a mother does in her kitchen, and then we fill it in special FDA-approved pots and seal all the goodness inside with special FDA-approved film. This way, we block off all the external air from the product inside. This sealed pack then goes in the sterilizer which takes it through a slightly higher temperature, very much like in your home cooking, but a much higher pressure which neutralizes all the bacteria present inside the food. So at the end of it, you get a pack of food that has no environmental exposure and no organisms left active within the food. <br/><br/>
				The sealed product can be stored for up to three months without refrigeration. Once you open the pack, it starts behaving like normal home-cooked food and thus, should be used how you would use home-cooked food – you can keep the opened pack for up to two days in your refrigerator.</p>
